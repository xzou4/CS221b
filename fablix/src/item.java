//item class for item in shopping cart
public class item {
	
	public int id;
	public int price;
	public int number;
	
	public item(int id, int price)
	{
		this.id = id;
		this.price = price;
		number = 1;
	}
	
	public item(int id)
	{
		this.id = id;
		price = 1;
		number = 1;
	}
	
	public int getId()
	{
		return id;
	}
	
	public int getNumber()
	{
		return number;
	}
	
	public void setNumber(int newnumber)
	{
		number = newnumber;
	}
}
